public class BasicInt {

	public static boolean isPositive(int a){
		if(a >= 0) {
			if(a > 0) {
				return true;
			} else {
				return true;
			}				
		} else {
			if(a < -1) {
				return false;
			} else {
				if(a < -19) {
					return false;
				} else {
					return false;
				}
			}
		}
	}
}
